algo = input('digite algo: ')
print('O tipo primitivo é: ', '\033[36m', type(algo), '\033[m')
print('e somente espaços? ', '\033[30:47m', algo.isspace(), '\033[m')
print('e um nr? ', '\033[31m', algo.isnumeric(), '\033[m')
print('e alfabetico? ', '\033[32m', algo.isalpha(), '\033[m')
print('e um nr e alfabetico? ', '\033[34m', algo.isalnum(), '\033[m')
print('esta em maiusculas? ', '\033[35m', algo.isupper(), '\033[m')
print('esta em minusculas? ', '\033[33m', algo.islower(), '\033[m')
print('esta capitalizada? ', '\033[37m', algo.istitle(), '\033[m')
